std::deque<Cell> setStageWall(int stage)
{
    std::deque<Cell> tmpWall;

    for(int i=0; i<MAX_ROW; i++){
        Cell temp;
        temp.p.row = i;
        for(int j=0; j<MAX_COL; j++){
            temp.p.col = j;

            if(MAP[stage-1][i][j] == 2){
                temp.type = IMMUNEWALL;
                tmpWall.push_back(temp);
            }else if(MAP[stage-1][i][j] == 0){
                temp.type = WALL;
                tmpWall.push_back(temp);
            }
        }
    }

    return tmpWall;
}
